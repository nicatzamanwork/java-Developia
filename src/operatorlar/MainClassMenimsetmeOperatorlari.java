package operatorlar;

public class MainClassMenimsetmeOperatorlari {

	public static void main(String[] args) {
		int age=44;
		System.out.println("age = "+age);
		
		age +=5;
		System.out.println("age = "+age);
		
		age -=3;
		System.out.println("age = "+age);
		
		age *=2;
		System.out.println("age = "+age);
		
		age %=4;
		System.out.println("age ="+age);
	}
}
